`timescale 1ns/1ps

module tb_alu_system_v3;

parameter WIDTH = 8;

reg clk;
reg rst_n;
reg valid;

reg [2:0] opcode;
reg [WIDTH-1:0] A;
reg [WIDTH-1:0] B;

wire [WIDTH-1:0] result;
wire carry;
wire valid_out;
wire [31:0] toggle_count;

//====================================================
// DUT
//====================================================

alu_system_v3 DUT
(
    .clk(clk),
    .rst_n(rst_n),
    .valid(valid),
    .opcode(opcode),
    .A(A),
    .B(B),
    .result(result),
    .carry(carry),
    .valid_out(valid_out),
    .toggle_count(toggle_count)
);

//====================================================
// CLOCK
//====================================================

initial
begin
    clk = 0;
    forever #5 clk = ~clk;      //100 MHz
end

//====================================================
// DUMP FILE
//====================================================

initial
begin
    $dumpfile("alu_v3.vcd");
    $dumpvars(0,tb_alu_system_v3);
end

//====================================================
// STIMULUS
//====================================================

initial
begin

    //------------------------------------------------
    // RESET
    //------------------------------------------------

    rst_n  = 0;
    valid  = 0;
    opcode = 0;
    A      = 0;
    B      = 0;

    #20;
    rst_n = 1;

    //------------------------------------------------
    // ADD
    //------------------------------------------------

    @(posedge clk);
    valid  = 1;
    opcode = 3'b000;
    A      = 8'd15;
    B      = 8'd10;

    #10;

    //------------------------------------------------
    // SUB
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b001;
    A      = 8'd50;
    B      = 8'd20;

    #10;

    //------------------------------------------------
    // AND
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b010;
    A      = 8'hAA;
    B      = 8'h55;

    #10;

    //------------------------------------------------
    // OR
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b011;
    A      = 8'hAA;
    B      = 8'h55;

    #10;

    //------------------------------------------------
    // XOR
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b100;
    A      = 8'hF0;
    B      = 8'h0F;

    #10;

    //------------------------------------------------
    // SHIFT LEFT
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b101;
    A      = 8'h11;

    #10;

    //------------------------------------------------
    // SHIFT RIGHT
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b110;
    A      = 8'h80;

    #10;

    //------------------------------------------------
    // COMPARE
    //------------------------------------------------

    @(posedge clk);
    opcode = 3'b111;
    A      = 8'd25;
    B      = 8'd15;

    #10;

    //------------------------------------------------
    // FORCE IDLE
    //------------------------------------------------

    valid = 0;

    repeat(12)
        @(posedge clk);

    //------------------------------------------------
    // WAKEUP TEST
    //------------------------------------------------

    valid  = 1;
    opcode = 3'b000;
    A      = 8'd100;
    B      = 8'd50;

    repeat(5)
        @(posedge clk);

    //------------------------------------------------
    // DISPLAY RESULTS
    //------------------------------------------------

    $display("--------------------------------");
    $display("Toggle Count = %d",toggle_count);
    $display("Final Result = %h",result);
    $display("--------------------------------");

    #50;

    $finish;

end

//====================================================
// MONITOR
//====================================================

initial
begin

    $monitor(
    "T=%0t VALID=%b OPCODE=%b A=%h B=%h RESULT=%h CARRY=%b TOGGLES=%d",
    $time,
    valid,
    opcode,
    A,
    B,
    result,
    carry,
    toggle_count
    );

end

endmodule
