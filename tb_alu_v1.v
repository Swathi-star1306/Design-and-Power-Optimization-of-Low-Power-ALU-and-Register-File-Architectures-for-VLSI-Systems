`timescale 1ns/1ps

module tb_alu_system;

parameter WIDTH = 8;

reg clk;
reg rst_n;

reg valid;

reg [2:0] opcode;
reg [WIDTH-1:0] A;
reg [WIDTH-1:0] B;

reg wr_en;
reg [2:0] wr_addr;

wire [WIDTH-1:0] result;
wire carry;
wire valid_out;

wire [31:0] toggle_count;

//==========================================================
// DUT
//==========================================================

alu_system dut
(
    .clk(clk),
    .rst_n(rst_n),

    .valid(valid),

    .opcode(opcode),
    .A(A),
    .B(B),

    .wr_en(wr_en),
    .wr_addr(wr_addr),

    .result(result),
    .carry(carry),
    .valid_out(valid_out),

    .toggle_count(toggle_count)
);

//==========================================================
// CLOCK
//==========================================================

always #5 clk = ~clk;

//==========================================================
// STIMULUS
//==========================================================

initial begin

    //------------------------------------------------------
    // LOW-VCD SAFE DUMP
    //------------------------------------------------------

    $dumpfile("alu_system.vcd");

    // LIMITED HIERARCHY DUMP
    $dumpvars(1, tb_alu_system);

    //------------------------------------------------------
    // INIT
    //------------------------------------------------------

    clk         = 1'b0;
    rst_n       = 1'b0;

    valid       = 1'b0;

    opcode      = 3'b000;

    A           = 8'd0;
    B           = 8'd0;

    wr_en       = 1'b0;
    wr_addr     = 3'd0;

    //------------------------------------------------------
    // RESET
    //------------------------------------------------------

    #20;
    rst_n = 1'b1;

    //------------------------------------------------------
    // ADDITION
    //------------------------------------------------------

    #10;

    valid       = 1'b1;

    opcode      = 3'b000;

    A           = 8'd15;
    B           = 8'd10;

    wr_en       = 1'b1;
    wr_addr     = 3'd1;

    //------------------------------------------------------
    // SUBTRACTION
    //------------------------------------------------------

    #20;

    opcode      = 3'b001;

    A           = 8'd50;
    B           = 8'd20;

    //------------------------------------------------------
    // XOR
    //------------------------------------------------------

    #20;

    opcode      = 3'b100;

    A           = 8'hAA;
    B           = 8'h55;

    //------------------------------------------------------
    // SHIFT LEFT
    //------------------------------------------------------

    #20;

    opcode      = 3'b101;

    A           = 8'h0F;

    //------------------------------------------------------
    // COMPARE
    //------------------------------------------------------

    #20;

    opcode      = 3'b111;

    A           = 8'd25;
    B           = 8'd10;

    //------------------------------------------------------
    // END
    //------------------------------------------------------

    #30;

    $display("====================================");
    $display("SIMULATION COMPLETED");
    $display("RESULT        = %d", result);
    $display("TOGGLE COUNT  = %d", toggle_count);
    $display("====================================");

    $finish;

end

endmodule
